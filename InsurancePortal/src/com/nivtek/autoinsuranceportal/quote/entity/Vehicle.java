package com.nivtek.autoinsuranceportal.quote.entity;

/**
 * @author Sartha Tamang
 *
 */
public class Vehicle {

	// Instance Variables

	private String vin;
	private String make;

	// Getters & Setters

	public String getVin() {
		return vin;
	}

	public void setVin(String vin) {
		this.vin = vin;
	}

	public String getMake() {
		return make;
	}

	public void setMake(String make) {
		this.make = make;
	}

}
